// APPLY LANGUAGE
function applyLang(code) {
  var dict = T[code];
  if (!dict) return;
  document.getElementById('langCode').textContent = code;
  document.querySelectorAll('.lang-opt').forEach(function(b) { b.classList.toggle('active', b.textContent === code); });
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });
  // Nav links
  var navIds = {'nav-stay':'fc1Title','nav-active':'fc2d','nav-contact':'fc3b'};
  Object.keys(navIds).forEach(function(id) {
    var el = document.getElementById(id);
    if (el && dict[navIds[id]] !== undefined) el.textContent = dict[navIds[id]];
  });
  document.title = (dict.pageTitle || 'Pitches') + ' - Medora Orbis';
  localStorage.setItem('lang', code);
}

